#include "libft.h"

int	ft_atoi(const char *s1)
{	
	int		num;
	short	sgn;

	num = 0;
	sgn = 1;
	while (*s1 == ' ' || *s1 == '\n' || *s1 == '\f' || *s1 == '\t'
		|| *s1 == '\v' || *s1 == '\r')
		s1++;
	if (*s1 == '-' || *s1 == '+')
	{
		if (*s1 == '-')
			sgn = -1;
		s1++;
	}
	while (ft_isdigit((int) *s1))
		num = (num * 10) + (*s1++ - '0');
	return (num * sgn);
}
